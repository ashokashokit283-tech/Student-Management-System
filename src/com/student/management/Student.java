package com.student.management;

public class Student {

    int id;
    String name;
    int age;
    String department;
    int marks;

    public Student(int id, String name, int age, String department, int marks) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.department = department;
        this.marks = marks;
    }
}
